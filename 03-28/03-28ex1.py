num1=10    #num1 변수에 정수 10 지정
num2=5     #num2 변수에 정수 5 지정

res1=num1*num2 #res1 변수에 num1*num2 경과 저장
res2=num1/num2 #res2 변수에 num1/num2 경과 저장
res3=num1//num2 #res3 변수에 num1//num2 경과 저장
res4=num1%num2 #res4 변수에 num1%num2 경과 저장

print("num1 * num2 =", res1)
print("num1 / num2 =", res2)
print("num1 // num2 =", res3)
print("num1 % num2 =", res4)