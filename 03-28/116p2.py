'''
2023.03.28
김수종
윗변이 23cm, 아랫변이 34cm, 높이가 13cm 인 사다리꼴의 넓이 구하기
변수 : 윗변, 아랫변, 높이
수식 : 사다리꼴 넓이 = ( ( 윗변 + 아랫변 ) * 높이 ) / 2
알고리즘 : 변수선언, 사다리꼴 넓이 구하기, 결과 출력
'''
top=23 #윗변
btm=34 #아랫변
hig=13 #높이
area=0 #사다리꼴 넓이

area=((top+btm)*hig)/2 #사다리꼴 넓이 구하는 수식

print("윗변이",top,"아랫변이",btm,"높이가",hig,"인 사다리꼴의 넓이 :",area)

